    <li class="ml-4"><strong>Due diligence time:</strong> Period to complete legal and financial checks</li>
    <li class="ml-4"><strong>Mortgage arrangement:</strong> Time to secure financing approval</li>
    <li class="ml-4"><strong>Legal preparation:</strong> Opportunity to engage lawyers and complete searches</li>
  </ul>

  <h3 class="text-xl font-semibold mb-4 mt-8 text-foreground">Typical Terms and Conditions</h3>
  
  <p class="mb-4 text-muted-foreground leading-relaxed">Reservation contracts typically require a deposit of 3,000-6,000 euros and provide 2-4 weeks to complete preparations. The contract specifies conditions under which either party can withdraw and the consequences of doing so.</p>

  <ul class="feature-list list-disc list-inside mb-6 space-y-2 text-muted-foreground">
