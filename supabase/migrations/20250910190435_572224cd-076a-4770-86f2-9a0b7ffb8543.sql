  <h3 class="text-xl font-semibold mb-4 mt-8 text-foreground">Legal Protections</h3>
  
  <p class="mb-4 text-muted-foreground leading-relaxed">A well-drafted reservation contract should include protective clauses that allow withdrawal without penalty under specified circumstances, such as mortgage refusal, unsatisfactory legal searches, or structural survey issues.</p>

  <ul class="feature-list list-disc list-inside mb-6 space-y-2 text-muted-foreground">
