  <p class="mb-4 text-muted-foreground leading-relaxed">A reservation contract (contrato de reserva or contrato de arras) is the first formal agreement in a Spanish property transaction. It secures your chosen property while allowing time to arrange financing, complete legal due diligence, and prepare for the main purchase contract.</p>

  <h3 class="text-xl font-semibold mb-4 mt-8 text-foreground">Purpose and Benefits</h3>
  
